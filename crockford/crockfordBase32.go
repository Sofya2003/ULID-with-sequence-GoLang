package crockford

func Encode(line string) string {
	symbols := map[string]string{
		"00000": "0", "00001": "1", "00010": "2", "00011": "3", "00100": "4", "00101": "5",
		"00110": "6", "00111": "7", "01000": "8", "01001": "9", "01010": "A", "01011": "B",
		"01100": "C", "01101": "D", "01110": "E", "01111": "F", "10000": "G", "10001": "H",
		"10010": "J", "10011": "K", "10100": "M", "10101": "N", "10110": "P", "10111": "Q",
		"11000": "R", "11001": "S", "11010": "T", "11011": "V", "11100": "W", "11101": "X",
		"11110": "Y", "11111": "Z",
	}
	line1 := []rune(line)
	encodedStr := ""
	c := 0
	s := ""
	for i := 0; i < len(line1); i++ {
		c += 1
		s += string(line1[i])
		if c == 5 {
			encodedStr += symbols[s]
			c = 0
			s = ""
		}
	}
	return encodedStr
}
